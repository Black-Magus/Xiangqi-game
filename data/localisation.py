def t(settings, key: str) -> str:
    # Return the translation for the current language, falling back to English
    lang = getattr(settings, "language", "en")
    # language dict or fallback to english
    lang_dict = TEXT.get(lang)
    if lang_dict is None:
        lang_dict = TEXT.get("en", {})
    # return requested key or fallback to english or the key itself
    return lang_dict.get(key, TEXT.get("en", {}).get(key, key))


